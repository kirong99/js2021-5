// 홀수짝수 알아보기 switch문

let input = 32; //값을 입력함

switch (input % 2) { // 2로나눴을때 나머지가..
    case 0 : // 0일때
        console.log("짝수 입니다.");
        break;
    case 1 : // 1일때 
        console.log("홀수 입니다.");
        break;
} 



